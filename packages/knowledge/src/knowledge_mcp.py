    def _format_knowledge_pack(self, guides: list[dict], max_tokens: int = 800) -> str:
        """Format loaded guides into a compact injection block within the token budget.

        Budget is split evenly across guides.  1 token ≈ 4 characters.
        """
        char_budget = max_tokens * 4
        per_guide = char_budget // max(len(guides), 1)
        sections = [self._format_single_guide(g, char_limit=per_guide) for g in guides]
        return "\n\n".join(sections)

    def _format_single_guide(self, guide: dict, char_limit: int = 1600) -> str:
        """Render one guide as a compact, token-efficient plain-text block.

        Uses bullet points and short labels so the LLM parses it naturally.
        Truncates on a line boundary if the content exceeds ``char_limit`` characters.
        """
        lines: list[str] = []

        title = guide.get("title", guide.get("slug", "Expert Knowledge"))
        books = "; ".join(guide.get("source_books", [])[:3])
        lines.append(f"=== SYNTHESISED KNOWLEDGE: {title} ===")
        if books:
            lines.append(f"Source: {books}")

        principles = guide.get("core_principles", [])
        if principles:
            lines.append("\nCORE PRINCIPLES:")
            for p in principles[:3]:
                principle = p.get("principle", "")
                source = p.get("source", "")
                src_note = f" ({source})" if source else ""
                lines.append(f"• {principle}{src_note}")

        steps = guide.get("process_steps", [])
        if steps:
            lines.append("\nHOW TO EXECUTE:")
            for s in steps[:5]:
                phase = s.get("phase", f"Step {s.get('step', '')}")
                timing = s.get("timing", "")
                timing_note = f" [{timing}]" if timing else ""
                actions = s.get("actions", [])
                action_str = "; ".join(actions[:2]) if actions else s.get("objective", "")
                lines.append(f"{s.get('step', '•')}. {phase}{timing_note}: {action_str}")

        rules = guide.get("decision_rules", [])
        if rules:
            lines.append("\nDECISION RULES:")
            for r in rules[:4]:
                lines.append(f"• {r}")

        sg = guide.get("singapore_adaptations", [])
        if sg:
            lines.append("\nSINGAPORE:")
            for item in sg[:3]:
                lines.append(f"• {item}")

        mistakes = guide.get("common_mistakes", [])
        if mistakes:
            lines.append("\nAVOID:")
            for m in mistakes[:3]:
                lines.append(f"• {m}")

        metrics = guide.get("success_metrics", {})
        if metrics:
            metric_str = " | ".join(f"{k}: {v}" for k, v in list(metrics.items())[:3])
            lines.append(f"\nBENCHMARKS: {metric_str}")

        full = "\n".join(lines)
        if len(full) <= char_limit:
            return full

        # Truncate on a line boundary.
        truncated = full[:char_limit]
        last_newline = truncated.rfind("\n")
        if last_newline >= 0:
            truncated = truncated[:last_newline]
        return truncated + "\n[truncated to fit token budget]"

    def _summarise_framework(self, content: object, name: str) -> str:
        """Create a concise text summary of a framework for search results."""
        if isinstance(content, dict):
            # Take first 3 keys and their descriptions
            lines = [f"Framework: {name}"]
            for i, (k, v) in enumerate(content.items()):
                if i >= 3:
                    break
                if isinstance(v, dict):
                    desc = v.get("description", str(v)[:200])
                elif isinstance(v, str):
                    desc = v[:200]
                else:
                    desc = str(v)[:200]
                lines.append(f"  {k}: {desc}")
            return "\n".join(lines)
        elif isinstance(content, list):
            lines = [f"Framework: {name} — {len(content)} principles"]
            for i, item in enumerate(content[:3]):
                if isinstance(item, dict):
                    principle = item.get("principle", item.get("name", f"Item {i + 1}"))
                    desc = item.get("description", "")[:150]
                    lines.append(f"  {i + 1}. {principle}: {desc}")
                else:
                    lines.append(f"  {i + 1}. {str(item)[:150]}")
            return "\n".join(lines)
        else:
            return f"Framework: {name}\n{str(content)[:400]}"


