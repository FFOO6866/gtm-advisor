    # ------------------------------------------------------------------
    # Layer 3: Semantic search over Qdrant
    # ------------------------------------------------------------------

    async def search_knowledge(
        self,
        query: str,
        agent_context: str | None = None,
        limit: int = 8,
    ) -> list[dict]:
        """Semantic search over all book chunks in the Qdrant marketing_knowledge collection.

        Falls back to returning relevant Layer 1 framework excerpts if Qdrant
        or OpenAI is not configured.

        Args:
            query: Natural language query, e.g. 'how to handle price objections in Singapore'.
            agent_context: Agent name (kebab-case) to filter results by relevance.
                If provided, results will be filtered to chunks tagged for this agent.
            limit: Maximum number of results to return.

        Returns:
            List of dicts with keys: book_title, chapter, page_number, text, score, agent_tags.
        """
        qdrant = await self._get_qdrant()
        openai_client = await self._get_openai()

        if qdrant is None or openai_client is None:
            logger.info(
                "Qdrant/OpenAI not configured — falling back to Layer 1 framework search for: %s",
                query,
            )
            return self._layer1_fallback_search(query, agent_context, limit)

        # Embed the query
        query_embedding = await self._embed_query(query, openai_client)
        if query_embedding is None:
            return self._layer1_fallback_search(query, agent_context, limit)

        try:
            from qdrant_client.models import FieldCondition, Filter, MatchAny  # noqa: PLC0415

            # Build optional agent filter
            search_filter = None
            if agent_context:
                agent_normalised = agent_context.replace("-", "_")
                search_filter = Filter(
                    must=[
                        FieldCondition(
                            key="agent_tags",
                            match=MatchAny(any=[agent_normalised]),
                        )
                    ]
                )

            response = await qdrant.query_points(
                collection_name="marketing_knowledge",
                query=query_embedding,
                query_filter=search_filter,
                limit=limit,
                with_payload=True,
            )

            results = []
            for hit in response.points:
                payload = hit.payload or {}
                results.append({
                    "book_title": payload.get("book_title", ""),
                    "book_key": payload.get("book_key", ""),
                    "chapter": payload.get("chapter"),
                    "page_number": payload.get("page_number", 0),
                    "text": payload.get("text", ""),
                    "score": round(hit.score, 4),
                    "agent_tags": payload.get("agent_tags", []),
                    "source": "qdrant_rag",
                })

            if not results:
                # Qdrant returned nothing — fall back to Layer 1
                return self._layer1_fallback_search(query, agent_context, limit)

            return results

        except Exception as exc:
            logger.warning("Qdrant search failed: %s — using Layer 1 fallback", exc)
            return self._layer1_fallback_search(query, agent_context, limit)

    # ------------------------------------------------------------------
    # Domain guides: synthesized knowledge from book chunks
    # ------------------------------------------------------------------

    async def get_domain_guide(self, slug: str) -> dict | None:
        """Load a synthesized domain guide from the guide store.

        Guides are JSON files generated by ``scripts/synthesize_domain_guides.py``,
        which retrieves Qdrant book chunks for a topic and uses GPT-4o to synthesize
        them into a structured operational guide.  When a guide file does not exist
        (because the synthesis script has not been run yet), this method returns
        ``None`` silently — agents should fall back to existing Layer 1 behaviour.

        Args:
            slug: Guide identifier, e.g. ``'digital_awareness_campaign'``,
                ``'cold_email_sequence'``, ``'icp_development'``.

        Returns:
            Guide dict with keys ``title``, ``source_books``, ``core_principles``,
            ``process_steps``, ``decision_rules``, ``singapore_adaptations``,
            ``common_mistakes``, ``success_metrics``, or ``None`` if not found.
        """
        # Guard against path traversal in slug.
        if "/" in slug or "\\" in slug or ".." in slug:
            return None
        # Prefer the _live.json variant (incrementally re-synthesized with
        # accumulated research) over the original static guide.
        live_path = _GUIDES_DIR / f"{slug}_live.json"
        base_path = _GUIDES_DIR / f"{slug}.json"
        guide_path = live_path if live_path.exists() else base_path
        if not guide_path.exists():
            logger.debug(
                "Domain guide '%s' not found — run scripts/synthesize_domain_guides.py", slug
            )
            return None
        try:
            return json.loads(guide_path.read_text(encoding="utf-8"))
        except Exception as exc:
            logger.warning("Failed to load domain guide '%s': %s", slug, exc)
            return None

    async def list_available_guides(self) -> list[str]:
        """Return slugs of all synthesized guides currently in the guide store.

        Excludes ``_live.json`` variants — they are transparent alternates loaded
        by ``get_domain_guide()`` and should not be listed as separate entries.
        """
        if not _GUIDES_DIR.exists():
            return []
        return sorted(
            p.stem for p in _GUIDES_DIR.glob("*.json") if not p.stem.endswith("_live")
        )

    async def get_agent_knowledge_pack(
        self,
        agent_name: str,
        task_context: str = "",
        max_tokens: int = 800,
    ) -> dict:
        """Select and format the most relevant domain guides for an agent's task.

        Scores each candidate guide by keyword overlap with ``task_context``,
        loads the top 2 available guides, and returns a compact formatted injection
        block suitable for prepending to the agent's user message.

        Args:
            agent_name: Kebab-case agent name, e.g. ``'campaign-architect'``.
            task_context: Raw task string — used to select the most relevant guides
                by keyword overlap.  First 500 characters are used.
            max_tokens: Approximate token budget for the formatted injection block
                (1 token ≈ 4 characters).  Defaults to 800.

        Returns:
            Dict with keys:

            - ``'guides'``: list of loaded guide slugs.
            - ``'source_books'``: deduplicated list of source book titles.
            - ``'formatted_injection'``: ready-to-prepend expert knowledge block,
              empty string when no guides are available for this agent.
        """
        guide_slugs = _AGENT_GUIDE_MAP.get(agent_name, [])
        if not guide_slugs:
            return {"guides": [], "source_books": [], "formatted_injection": ""}

        task_lower = task_context[:500].lower()

        # Score each guide by keyword overlap with the task, preserving map order on ties.
        scored: list[tuple[int, int, str]] = []
        for idx, slug in enumerate(guide_slugs):
            keywords = _GUIDE_RELEVANCE_KEYWORDS.get(slug, slug.replace("_", " ").split())
            hits = sum(1 for kw in keywords if kw in task_lower)
            scored.append((hits, -idx, slug))
        scored.sort(reverse=True)

        # Try up to 4 candidates to load 2 available guides.
        loaded: list[dict] = []
        for _, _, slug in scored[:4]:
            if len(loaded) >= 2:
                break
            guide = await self.get_domain_guide(slug)
            if guide is not None:
                loaded.append(guide)

        if not loaded:
            return {"guides": [], "source_books": [], "formatted_injection": ""}

        # Deduplicate source books while preserving order.
        source_books: list[str] = list(
            dict.fromkeys(b for g in loaded for b in (g.get("source_books") or []))
        )
        return {
            "guides": [g["slug"] for g in loaded],
            "source_books": source_books,
            "formatted_injection": self._format_knowledge_pack(loaded, max_tokens=max_tokens),
        }

    async def synthesize_guide_incremental(
        self,
        slug: str,
        extra_texts: list[str] | None = None,
    ) -> bool:
        """Re-synthesize a guide using Qdrant book chunks + supplementary texts.

        Saves the result as ``{slug}_live.json`` — the original guide is never
        overwritten.  ``get_domain_guide()`` automatically prefers the live
        variant when it exists.

        Uses the already-initialised Qdrant and OpenAI clients (no external
        process, no file-lock conflict with the running gateway).

        Args:
            slug: Guide identifier, e.g. ``'cold_email_sequence'``.
            extra_texts: Optional list of supplementary research texts
                (e.g. accumulated ResearchCache content) to include alongside
                the book chunks in the synthesis prompt.

        Returns:
            ``True`` if a new ``{slug}_live.json`` was written, ``False`` on
            any failure (logged but never raised).
        """
        try:
            # Guard against path traversal in slug.
            if "/" in slug or "\\" in slug or ".." in slug:
                logger.warning("synthesize_guide_incremental: invalid slug '%s'", slug)
                return False

            # Load the existing guide to get search_queries and metadata.
            base_path = _GUIDES_DIR / f"{slug}.json"
            if not base_path.exists():
                logger.warning("synthesize_guide_incremental: base guide '%s' not found", slug)
                return False
            base_guide = json.loads(base_path.read_text(encoding="utf-8"))
            search_queries: list[str] = base_guide.get("search_queries", [])
            if not search_queries:
                # Derive from slug keywords as fallback.
                keywords = _GUIDE_RELEVANCE_KEYWORDS.get(slug, slug.replace("_", " ").split())
                search_queries = [" ".join(keywords)]

            title = base_guide.get("title", slug.replace("_", " ").title())
            agents_str = ", ".join(base_guide.get("agent_relevance", []))

            # Collect book chunks from Qdrant via embedding search.
            qdrant = await self._get_qdrant()
            openai = await self._get_openai()
            if qdrant is None or openai is None:
                logger.warning("synthesize_guide_incremental: Qdrant or OpenAI not available")
                return False

            seen_ids: set[str] = set()
            chunks: list[dict] = []
            for query in search_queries[:5]:
                try:
                    resp = await openai.embeddings.create(
                        model="text-embedding-3-small",
                        input=query.strip()[:6000],
                    )
                    embedding = resp.data[0].embedding
                    results = await qdrant.query_points(
                        collection_name="marketing_knowledge",
                        query=embedding,
                        limit=8,
                        with_payload=True,
                    )
                    for hit in results.points:
                        chunk_id = str(hit.id)
                        if chunk_id in seen_ids:
                            continue
                        seen_ids.add(chunk_id)
                        payload = hit.payload or {}
                        chunks.append({
                            "book_title": payload.get("book_title", "Unknown"),
                            "chapter": payload.get("chapter"),
                            "page_number": payload.get("page_number", 0),
                            "text": payload.get("text", ""),
                            "score": round(hit.score, 4),
                        })
                except Exception:
                    logger.debug("synthesize_guide_incremental: query failed", exc_info=True)

            chunks.sort(key=lambda c: c["score"], reverse=True)
            chunks = chunks[:20]

            if not chunks:
                logger.warning("synthesize_guide_incremental: no book chunks for '%s'", slug)
                return False

            # Build excerpts text (book chunks + extra research).
            book_titles = list(dict.fromkeys(c["book_title"] for c in chunks))
            excerpt_parts: list[str] = []
            for i, c in enumerate(chunks, 1):
                ch = f", {c['chapter']}" if c.get("chapter") else ""
                excerpt_parts.append(
                    f"[{i}] {c['book_title']}{ch} (p.{c['page_number']}, "
                    f"score={c['score']}):\n{c['text'][:800]}"
                )

            # Append supplementary research texts.
            extra = extra_texts or []
            for j, text in enumerate(extra[:10], len(chunks) + 1):
                excerpt_parts.append(
                    f"[{j}] RECENT MARKET RESEARCH (live data):\n{text[:800]}"
                )
            book_count = len(book_titles) + (1 if extra else 0)

            excerpts = "\n\n".join(excerpt_parts)
            must_cover = "\n".join(
                f"  - {item}" for item in base_guide.get("must_cover", [])
            ) or "  - Cover all key aspects of the topic"

            # Synthesis prompt — matches the format in synthesize_domain_guides.py
            # so incremental guides have consistent quality with initial ones.
            system_prompt = (
                "You are a marketing knowledge synthesizer. Your task is to extract "
                "structured, actionable operational guidance from real book excerpts "
                "— not from your general training knowledge.\n\n"
                "STRICT RULES:\n"
                "1. Every item in core_principles MUST cite its exact source book "
                "from the excerpts provided.\n"
                "2. Do NOT use knowledge not present in the provided excerpts — "
                'mark gaps as "(inferred — verify with source)".\n'
                "3. All text fields must be under 130 characters each "
                "(for token efficiency).\n"
                "4. decision_rules must follow strict IF/WHEN → action format.\n"
                "5. singapore_adaptations must be specific and practical, "
                "not generic.\n"
                "6. Return valid JSON ONLY — no markdown fences, no explanation "
                "text outside the JSON."
            )
            user_prompt = (
                f"GUIDE TOPIC: {title}\nAGENT USERS: {agents_str}\n"
                f"WHAT THIS GUIDE MUST COVER:\n{must_cover}\n\n"
                f"BOOK EXCERPTS ({len(chunks) + len(extra)} passages from "
                f"{book_count} sources):\n---\n{excerpts}\n---\n\n"
                "Synthesize ONLY from the excerpts above into this exact JSON "
                "structure:\n"
                "{\n"
                '  "core_principles": [\n'
                '    {"principle": "concise statement", "source": "exact book '
                'title", "application": "how to apply"}\n'
                "  ],\n"
                '  "process_steps": [\n'
                '    {"step": 1, "phase": "name", "objective": "goal", '
                '"actions": ["action1"], "timing": "Week 1-2"}\n'
                "  ],\n"
                '  "decision_rules": ["If [condition]: [action]"],\n'
                '  "singapore_adaptations": ["Specific SG adaptation"],\n'
                '  "common_mistakes": ["Specific mistake"],\n'
                '  "success_metrics": {"metric": "target value"}\n'
                "}\n\n"
                "REQUIREMENTS:\n"
                "- 3-5 core_principles, each citing a source book\n"
                "- 4-8 process_steps covering the end-to-end workflow\n"
                "- 5-8 decision_rules as IF/WHEN → action statements\n"
                "- 3-5 singapore_adaptations for Singapore B2B SME context\n"
                "- 3-5 common_mistakes (concrete, not vague)\n"
                "- 3-5 success_metrics with quantitative targets"
            )

            response = await openai.chat.completions.create(
                model="gpt-4o",
                response_format={"type": "json_object"},
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_prompt},
                ],
                temperature=0.1,
                max_tokens=2000,
            )
            raw = response.choices[0].message.content or "{}"
            synthesized = json.loads(raw)

            # Validate that the synthesis produced meaningful content.
            # If the new guide has fewer core items than the base, the
            # Qdrant retrieval was incomplete — keep the original guide.
            required_keys = (
                "core_principles", "process_steps", "decision_rules",
                "singapore_adaptations", "common_mistakes",
            )
            for key in required_keys:
                if not isinstance(synthesized.get(key), list) or not synthesized[key]:
                    logger.warning(
                        "synthesize_guide_incremental_validation_failed slug=%s missing_key=%s",
                        slug,
                        key,
                    )
                    return False

            base_principles = len(base_guide.get("core_principles", []))
            new_principles = len(synthesized.get("core_principles", []))
            if base_principles > 0 and new_principles < base_principles * 0.6:
                logger.warning(
                    "synthesize_guide_incremental_content_loss slug=%s base=%d new=%d",
                    slug,
                    base_principles,
                    new_principles,
                )
                return False

            from datetime import UTC  # noqa: PLC0415
            from datetime import datetime as _dt

            guide = {
                **synthesized,
                "slug": slug,
                "title": title,
                "search_queries": search_queries,
                "agent_relevance": base_guide.get("agent_relevance", []),
                "source_books": book_titles,
                "source_chunk_count": len(chunks),
                "extra_research_count": len(extra),
                "synthesized_at": _dt.now(UTC).isoformat(),
                "synthesis_type": "incremental",
            }

            live_path = _GUIDES_DIR / f"{slug}_live.json"
            _GUIDES_DIR.mkdir(parents=True, exist_ok=True)
            # Atomic write: write to tmp file then os.replace() to avoid
            # corrupt _live.json if the process is killed mid-write.
            import os  # noqa: PLC0415

            tmp_path = live_path.with_suffix(".tmp")
            tmp_path.write_text(
                json.dumps(guide, indent=2, ensure_ascii=False), encoding="utf-8"
            )
            os.replace(tmp_path, live_path)
            logger.info(
                "guide_resynthesized slug=%s chunks=%d extra=%d",
                slug,
                len(chunks),
                len(extra),
            )
            return True

        except Exception:
            logger.warning("synthesize_guide_incremental_failed slug=%s", slug, exc_info=True)
            return False

    async def get_sg_reference(
        self,
        query: str,
        category_type: str | None = None,
        limit: int = 5,
    ) -> list[dict]:
        """Return relevant Singapore government reference data for a query.

        Searches the sg_knowledge_articles DB table. Falls back to the static
        PSG_STATIC_FALLBACK list from the scraper module when the DB is empty.

        Args:
            query: Free-text search query (matched against title + summary).
            category_type: Optional filter — "psg_vendor", "grant_overview",
                "enforcement", "regulation", "compliance", "market_data".
            limit: Maximum rows to return.

        Returns:
            List of dicts with keys: title, summary, url, source, category_type,
            last_verified.
        """
        try:
            from sqlalchemy import select  # noqa: PLC0415

            from packages.database.src.models import SgKnowledgeArticle  # noqa: PLC0415
            from packages.database.src.session import async_session_factory  # noqa: PLC0415

            async with async_session_factory() as db:
                stmt = select(SgKnowledgeArticle).where(SgKnowledgeArticle.is_active == True)  # noqa: E712
                if category_type:
                    stmt = stmt.where(SgKnowledgeArticle.category_type == category_type)
                stmt = stmt.order_by(SgKnowledgeArticle.fetched_at.desc()).limit(limit * 3)
                rows = list((await db.execute(stmt)).scalars().all())

            if not rows:
                # DB empty — return static fallback filtered by keyword
                return self._filter_static_sg(query, category_type, limit)

            # Simple keyword ranking against query terms
            query_terms = set(query.lower().split())
            scored: list[tuple[int, dict]] = []
            for row in rows:
                text = f"{row.title} {row.summary or ''}".lower()
                hits = sum(1 for t in query_terms if t in text)
                scored.append((hits, {
                    "title": row.title,
                    "summary": row.summary or "",
                    "url": row.url,
                    "source": row.source,
                    "category_type": row.category_type,
                    "last_verified": row.last_verified or row.fetched_at.date().isoformat(),
                }))
            scored.sort(key=lambda x: x[0], reverse=True)
            return [item for _, item in scored[:limit]]

        except Exception:
            logger.warning("get_sg_reference_failed", exc_info=True)
            return self._filter_static_sg(query, category_type, limit)

    def _filter_static_sg(
        self,
        query: str,
        category_type: str | None,
        limit: int,
    ) -> list[dict]:
        """Return keyword-scored items from the static PSG fallback list."""
        try:
            from packages.integrations.sg_reference.src.scraper import (
                PSG_STATIC_FALLBACK,  # noqa: PLC0415
            )
        except ImportError:
            return []
        query_terms = set(query.lower().split())
        results: list[tuple[int, dict]] = []
        for item in PSG_STATIC_FALLBACK:
            if category_type and item.get("category_type") != category_type:
                continue
            text = f"{item.get('title', '')} {item.get('summary', '')}".lower()
            hits = sum(1 for t in query_terms if t in text)
            results.append((hits, item))
        results.sort(key=lambda x: x[0], reverse=True)
        return [item for _, item in results[:limit]]

