    async def get_framework(self, framework_name: str) -> dict:
        """Get a specific marketing framework by name.

        Args:
            framework_name: One of the constants in frameworks.py, e.g.
                'CIALDINI_PRINCIPLES', 'STP_FRAMEWORK', 'SALES_QUALIFICATION',
                'MESSAGING_FRAMEWORKS', 'OGILVY_PRINCIPLES', 'GTM_FRAMEWORKS',
                'ICP_FRAMEWORK', 'SINGAPORE_SME_CONTEXT', 'MADE_TO_STICK_SUCCESS',
                'CAMPAIGN_BRIEF_TEMPLATE', 'OBJECTION_HANDLING_FRAMEWORK',
                'RACE_FRAMEWORK', 'MARKETING_MIX_4P', 'PORTER_FIVE_FORCES'.

        Returns:
            The framework dict, plus metadata about its source books.
            Returns an error dict if framework_name is not found.
        """
        import packages.knowledge.src.frameworks as fw  # noqa: PLC0415
        from packages.knowledge.src.book_index import get_attribution_for_framework  # noqa: PLC0415

        framework = getattr(fw, framework_name, None)
        if framework is None:
            available = fw.ALL_FRAMEWORK_NAMES
            return {
                "error": f"Framework '{framework_name}' not found.",
                "available_frameworks": available,
            }

        attribution = get_attribution_for_framework(framework_name)
        return {
            "framework_name": framework_name,
            "content": framework,
            "source_books": [
                {"title": b["title"], "authors": b.get("authors", [])}
                for b in attribution
            ],
        }

