class KnowledgeMCPServer:
    """MCP server giving agents structured access to the marketing knowledge base.

    Layer 1 methods (get_framework, get_messaging_framework, get_campaign_brief_template,
    get_objection_handlers) are always available — they use static constants.

    Layer 3 methods (search_knowledge) require Qdrant and OpenAI to be configured.
    They degrade gracefully: if unavailable, they return relevant Layer 1 content instead.
    """

    def __init__(self, openai_api_key: str | None = None, qdrant_url: str | None = None) -> None:
        self._openai_api_key = openai_api_key or os.environ.get("OPENAI_API_KEY", "").strip()
        self._qdrant_url = qdrant_url or os.environ.get("QDRANT_URL", "").strip()
        self._qdrant_path = os.environ.get("QDRANT_PATH", "").strip()
        self._openai_client = None
        self._qdrant_client = None

