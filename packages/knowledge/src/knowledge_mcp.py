    async def get_objection_handlers(self, objection_type: str) -> list[dict]:
        """Get objection handling guidance based on Just Listen + Cialdini principles.

        Args:
            objection_type: One of: 'budget', 'timing', 'incumbent', 'authority',
                'trust', 'priority', or a free-form objection text.

        Returns:
            List of handling approach dicts with 'technique', 'example', 'principle'.
        """
        from packages.knowledge.src.frameworks import (  # noqa: PLC0415
            CIALDINI_PRINCIPLES,
            OBJECTION_HANDLING_FRAMEWORK,
            SINGAPORE_SME_CONTEXT,
        )

        objection_lower = objection_type.lower()
        sg_objections = SINGAPORE_SME_CONTEXT.get("common_objections", [])

        # Find matching Singapore-specific handling
        sg_handlers: list[dict] = []
        for obj_data in sg_objections:
            obj_text = obj_data.get("objection", "").lower()
            if any(word in objection_lower for word in obj_text.split()[:3]):
                sg_handlers.append({
                    "type": "singapore_specific",
                    "objection": obj_data.get("objection"),
                    "underlying_fear": obj_data.get("underlying_fear"),
                    "technique": obj_data.get("response_framework"),
                    "example": obj_data.get("sg_angle"),
                    "principle": "Goulston: acknowledge the fear, not just the words",
                })

        # Base handling from Goulston framework
        sequence = OBJECTION_HANDLING_FRAMEWORK.get("sequence", {})
        base_handlers = [
            {
                "type": "universal_sequence",
                "step": step_name,
                "technique": step_data.get("technique", ""),
                "description": step_data.get("description", ""),
                "principle": "Just Listen — Mark Goulston",
            }
            for step_name, step_data in sequence.items()
        ]

        # Add relevant Cialdini counter for budget/scarcity objections
        cialdini_handlers: list[dict] = []
        if any(w in objection_lower for w in ["budget", "cost", "price", "expensive"]):
            scarcity = CIALDINI_PRINCIPLES.get("scarcity", {})
            reciprocity = CIALDINI_PRINCIPLES.get("reciprocity", {})
            cialdini_handlers = [
                {
                    "type": "cialdini_counter",
                    "principle": "scarcity",
                    "technique": scarcity.get("application", ""),
                    "sg_example": scarcity.get("sg_angle", ""),
                },
                {
                    "type": "cialdini_counter",
                    "principle": "reciprocity",
                    "technique": reciprocity.get("application", ""),
                    "sg_example": reciprocity.get("sg_angle", ""),
                },
            ]
        elif any(w in objection_lower for w in ["trust", "credibility", "proven", "risk"]):
            authority = CIALDINI_PRINCIPLES.get("authority", {})
            social_proof = CIALDINI_PRINCIPLES.get("social_proof", {})
            cialdini_handlers = [
                {
                    "type": "cialdini_counter",
                    "principle": "authority",
                    "technique": authority.get("application", ""),
                    "sg_example": authority.get("sg_angle", ""),
                },
                {
                    "type": "cialdini_counter",
                    "principle": "social_proof",
                    "technique": social_proof.get("application", ""),
                    "sg_example": social_proof.get("sg_angle", ""),
                },
            ]

        return sg_handlers + base_handlers + cialdini_handlers

    async def get_singapore_context(self, topic: str | None = None) -> dict:
        """Return Singapore-specific GTM and SME market context.

        Args:
            topic: Optional topic filter, e.g. 'psg_grant', 'buying_triggers',
                'common_objections', 'channel_effectiveness'. If None, returns full context.

        Returns:
            Relevant Singapore SME context dict.
        """
        from packages.knowledge.src.frameworks import SINGAPORE_SME_CONTEXT  # noqa: PLC0415

        if topic is None:
            return SINGAPORE_SME_CONTEXT

        if topic in SINGAPORE_SME_CONTEXT:
            return {
                "topic": topic,
                "content": SINGAPORE_SME_CONTEXT[topic],
            }

        # Fuzzy match
        topic_lower = topic.lower()
        for key, value in SINGAPORE_SME_CONTEXT.items():
            if topic_lower in key.lower():
                return {"topic": key, "content": value}

        return {
            "topic": topic,
            "content": None,
            "available_topics": list(SINGAPORE_SME_CONTEXT.keys()),
        }

